package com.groupkk.ceke.fragment;

/**
 * 关于圈子的数据
 * @author Administrator
 *
 */
public class CircleData {
	String circleName;//圈子的名字
	String circleCulture;//圈子的文化
	String peopleNumber;//圈子里面的人数
	public String getCircleName() {
		return circleName;
	}
	public void setCircleName(String circleName) {
		this.circleName = circleName;
	}
	public String getCircleCulture() {
		return circleCulture;
	}
	public void setCircleCulture(String circleCulture) {
		this.circleCulture = circleCulture;
	}
	public String getPeopleNumber() {
		return peopleNumber;
	}
	public void setPeopleNumber(String peopleNumber) {
		this.peopleNumber = peopleNumber;
	}
}
